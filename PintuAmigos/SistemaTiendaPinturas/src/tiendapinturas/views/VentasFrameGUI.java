     private void agregarAlCarrito() {
        int selectedRow = tblProductos.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Seleccione un producto de la tabla", 
                "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            int productoId = (int) modelProductos.getValueAt(selectedRow, 0);
            String codigo = (String) modelProductos.getValueAt(selectedRow, 1);
            String nombre = (String) modelProductos.getValueAt(selectedRow, 2);
            double precio = Double.parseDouble(((String) modelProductos.getValueAt(selectedRow, 4)).replace("$", ""));
            int stock = (int) modelProductos.getValueAt(selectedRow, 5);
            
            int cantidad;
            try {
                cantidad = Integer.parseInt(txtCantidad.getText().trim());
                if (cantidad <= 0) throw new NumberFormatException();
            } catch (NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Ingrese una cantidad v√°lida", 
                    "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if (cantidad > stock) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Stock insuficiente. Disponible: " + stock, 
                    "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            for (int i = 0; i < carrito.size(); i++) {
                Producto p = carrito.get(i);
                if (p.getId() == productoId) {
                    int opcion = javax.swing.JOptionPane.showConfirmDialog(this,
                        "El producto ya est√° en el carrito. ¬øActualizar cantidad?",
                        "Producto duplicado", javax.swing.JOptionPane.YES_NO_OPTION);
                    
                    if (opcion == javax.swing.JOptionPane.YES_OPTION) {
                        carrito.set(i, new Producto(productoId, codigo, nombre, "", precio, cantidad));
                        actualizarCarrito();
                    }
                    return;
                }
            }
            
            Producto producto = new Producto(productoId, codigo, nombre, "", precio, cantidad);
            carrito.add(producto);
            actualizarCarrito();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error: " + e.getMessage(), 
                "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
     
