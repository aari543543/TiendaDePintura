    private void cargarHistorial() {
        DefaultTableModel model = (DefaultTableModel) tblHistorial.getModel();
        model.setRowCount(0);  // Limpiar tabla
        
        String sql = "SELECT " +
                     "v.venta_id, " +
                     "v.numero_ticket, " +
                     "v.fecha, " +
                     "c.nombre AS cliente, " +
                     "ve.nombre AS vendedor, " +
                     "v.total, " +
                     "v.metodo_pago, " +
                     "CASE WHEN v.total > 0 THEN 'COMPLETADA' ELSE 'PENDIENTE' END AS estado " +
                     "FROM ventas v " +
                     "LEFT JOIN clientes c ON v.cliente_id = c.cliente_id " +
                     "LEFT JOIN vendedores ve ON v.vendedor_id = ve.vendedor_id " +
                     "ORDER BY v.fecha DESC, v.venta_id DESC";
        
        try (Connection conn = DatabaseConnection.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {
            
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            
            while (rs.next()) {
                java.util.Date fecha = rs.getDate("fecha");
                String fechaStr = (fecha != null) ? dateFormat.format(fecha) : "N/A";
                
                Object[] row = {
                    rs.getInt("venta_id"),
                    rs.getString("numero_ticket"),
                    fechaStr,
                    rs.getString("cliente") != null ? rs.getString("cliente") : "N/A",
                    rs.getString("vendedor") != null ? rs.getString("vendedor") : "N/A",
                    String.format("$%.2f", rs.getDouble("total")),
                    rs.getString("metodo_pago"),
                    rs.getString("estado")
                };
                model.addRow(row);
            }
            
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error al cargar historial: " + e.getMessage(),
                "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
