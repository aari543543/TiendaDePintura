    private void guardarTicketArchivo(String contenido, String nombreArchivo) {
    // Crear selector de archivos
    javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
    
    // Sugerir nombre de archivo
    fileChooser.setSelectedFile(new java.io.File(nombreArchivo + ".txt"));
    
    // Mostrar di√°logo de guardar
    int resultado = fileChooser.showSaveDialog(this);
    
    if (resultado == javax.swing.JFileChooser.APPROVE_OPTION) {
        java.io.File archivo = fileChooser.getSelectedFile();
        
        // Asegurar extensi√≥n .txt
        if (!archivo.getName().toLowerCase().endsWith(".txt")) {
            archivo = new java.io.File(archivo.getAbsolutePath() + ".txt");
        }
        
        try (java.io.FileWriter writer = new java.io.FileWriter(archivo)) {
            writer.write(contenido);
            
            javax.swing.JOptionPane.showMessageDialog(this,
                "Ticket guardado exitosamente en:\n" + archivo.getAbsolutePath(),
                "√âxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
        } catch (java.io.IOException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error al guardar el archivo:\n" + e.getMessage(),
                "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
}
    
